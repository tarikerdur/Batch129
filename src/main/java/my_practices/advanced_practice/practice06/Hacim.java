package my_practices.advanced_practice.practice06;

public class Hacim {


    public int hacimHesapla(int a) { // kup
        return a * a * a;
    }

    public int hacimHesapla(int a, int b) { // kare prizma
        return a * a * b;
    }

    public int hacimHesapla(int a, int b, int c) { // dikdortgen prizma
        return a * b * c;
    }


}
